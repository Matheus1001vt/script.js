let dia = Number(prompt("Calendário semanal"))

if (dia == 1) {
    alert("domingo")
}

else if (dia == 2){
    alert("segunda")
}

else if (dia == 3){
    alert("terça")
}

else if (dia == 4){
    alert("quarta")
}

else if (dia == 5){
    alert("quinta")
}

else if (dia == 6){
    alert("sexta")
}

else if (dia == 7){
    alert("sabado")
}











